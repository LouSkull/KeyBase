def translate(key: str, lang: str = "en", **kwargs: object) -> str:
    """Return translated string for key in lang, falling back to English."""
    table = TRANSLATIONS.get(lang) or TRANSLATIONS["en"]
    text = table.get(key) or TRANSLATIONS["en"].get(key, key)
    for k, v in kwargs.items():
        text = text.replace(f"{{{k}}}", str(v))
    return text
